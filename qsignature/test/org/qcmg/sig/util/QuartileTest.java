	@Test
	public void testQuartile() {
		List<Number> numbers = getNumbers();
		assertEquals(763, numbers.size());
		
		double[] quartiles = Quartile.quartile(numbers);
		// Lynns values
		assertEquals(0.3016462, quartiles[0], 0.0001);
		assertEquals(0.3250978, quartiles[2], 0.0001);
		
		// actual
		assertEquals(0.301628452, quartiles[0], 0.0000001);
		assertEquals(0.325122996, quartiles[2], 0.0000001);
		
		// diff between the 2 quartiles
		double quartileDiffActual = quartiles[2] - quartiles[0];
		double quartileDiffLynn = 0.3250978 - 0.3016462;
		// Lynns values
		assertEquals(0.02345159, quartileDiffLynn, 0.000001);
		// actual
		assertEquals(0.0234945, quartileDiffActual, 0.0000001);
		
		double cutoffActual = quartileDiffActual * 2;
		double cutoffLynn = quartileDiffLynn * 2;
		// Lynns values
		assertEquals(0.04690319, cutoffLynn, 0.0000001);
		// actual
		assertEquals(0.046989088, cutoffActual, 0.0000001);
		
		int noOKeepers = 0;
		for (Number n : numbers) {
			double number = n.doubleValue();
			if (number < quartiles[0] - cutoffActual) noOKeepers++;
		}
		
		assertEquals(4, noOKeepers);
	}
	
